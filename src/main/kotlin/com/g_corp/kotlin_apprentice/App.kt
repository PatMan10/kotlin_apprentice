package com.g_corp.kotlin_apprentice

import kotlin.math.*

const val CONSTANT = 69
fun main(args: Array<String>) {
    println("%.0f".format(28.0 % 10.0))
    println("1 shl 3 = ${1 shl 3}")
    println("32 shr 2 = ${1 shl 3}")
    println("sin(45 * PI / 180) = ${sin(45 * PI / 180)}")
    println("PI = $PI\n\n\n")

    println("CONSTANT = $CONSTANT")
}
